// use switch statement to print the day of the week.

let day = 5;
console.log("enter number between 1 to 7")

switch(day){
    case 1:
        console.log("monday");
        break;
    case 2:
        console.log("tuesday");
        break;
    case 3:
        console.log("wedmesday");
        break;
    case 4:
        console.log("thursday");
        break;
    case 5:
        console.log("friday");
        break;
    case 6:
        console.log("satarday");
        break;
    case 7:
        console.log("Sunday");
        break;
    default:
        console.log("Enter valid number");
}