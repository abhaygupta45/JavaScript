// slice method
// it copies a portion of an array

let colors = ["red","green","orange","white","black","blue" ];

console.log("without using slice method : ",colors);
console.log("with slice method : ",colors.slice());
console.log("1 to 3: ",colors.slice(1,3));
console.log("4: ",colors.slice(4));
console.log("-2: ",colors.slice(-2  ));

